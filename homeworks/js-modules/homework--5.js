let firstNumber = Number(prompt('Введите первее число: одно из трех чисел для вычисление среднего арифметического числа'));
let secondNumber = Number(prompt('Введите второе число: одно из трех чисел для вычисление среднего арифметического числа'));
let thirdNumber = Number(prompt('Введите третье число: одно из трех чисел для вычисление среднего арифметического числа'));
